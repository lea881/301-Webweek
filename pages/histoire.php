                <p>
               Né en 1883, c’est à l’âge de dix ans que Morihei Ueshiba commence l’étude des arts martiaux. Au fil de sa vie, il devient un expert reconnu en ju-jitsu, ken-jutsu et daito-ryu, consacrant sa vie à perfectionner sa technique et sa compréhension des arts martiaux. Au cours de son apprentissage, il constate que le vrai intérêt des arts martiaux ne réside pas dans la victoire sur un adversaire par la force, mais dans la préservation de la paix, l’acceptation et le favorissement de l’épanouissement de tous les êtres.
                Vers 1925, après de nombreuses rencontres avec des maîtres tels que Sokaku Takeda et Wanisaburo Deguchi, ainsi que de multiples expériences techniques et spirituelles, il définit les principes immuables de l’Aïkido, donnant naissance à cet art martial unique. Connu sous le nom de O’Sensei, il intègre dans sa pratique une dimension spirituelle inspirée par la nature et l’énergie universelle, et conçoit l’Aïkido comme une voie de paix, d’harmonie et de non-violence, destinée à parfaire le corps et l’esprit.
                À sa mort en 1969, l’Aïkido s’était déjà répandu à travers le monde, pratiqué par des centaines de milliers de personnes sur les cinq continents, et reconnu comme un art martial alliant technique, humanisme et développement personnel.	
